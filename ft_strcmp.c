#include "libft.h"

int			ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	if (s1[i] == '\200' || s2[i] == '\200')
		return (s2[i] - s1[i]);
	else
		return (s1[i] - s2[i]);
}
